package lk.ac.mrt.cse.dbs.simpleexpensemanager.control;

import android.content.Context;

import lk.ac.mrt.cse.dbs.simpleexpensemanager.control.exception.ExpenseManagerException;
import lk.ac.mrt.cse.dbs.simpleexpensemanager.data.AccountDAO;
import lk.ac.mrt.cse.dbs.simpleexpensemanager.data.TransactionDAO;
import lk.ac.mrt.cse.dbs.simpleexpensemanager.data.impl.Db_helper;
import lk.ac.mrt.cse.dbs.simpleexpensemanager.data.impl.PersistentAccountDAO;
import lk.ac.mrt.cse.dbs.simpleexpensemanager.data.impl.PersistentTransactionDAO;

public class PersistentExpenceManager extends ExpenseManager{

    private final Context context;

    public PersistentExpenceManager(Context context) {
        this.context = context;
        try {
            setup();
        } catch (ExpenseManagerException e) {
            e.printStackTrace();
        }
    }

    @Override
    public void setup() throws ExpenseManagerException {
        Db_helper dbhelper = new Db_helper(context);

        TransactionDAO transDAO = new PersistentTransactionDAO(dbhelper) ;
        setTransactionsDAO(transDAO);

        AccountDAO perAccountDAO = new PersistentAccountDAO(dbhelper);
        setAccountsDAO(perAccountDAO);
    }
}
